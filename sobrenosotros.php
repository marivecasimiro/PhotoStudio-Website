                            <hr />
							<p>La fotografía es el <strong>arte de la luz</strong>, la pintura es el arte de la línea y el color. El fin es el mismo, pero el medio es totalmente distinto. La fotografía es el arte más social. </p>
							<p>Todo el mundo ha agarrado una cámara y ha hecho cientos de fotografías, ya sea en las vacaciones o en las fiestas familiares.
							Esta socialización, enormemente positiva, ha provocado un empobrecimiento general producto, no de la diversificación, sino de la falta de educación.</p> 
							<p>Todo el mundo, por el hecho de cargar con una cámara, se cree fotógrafo, y lo es, pero le falta la conciencia de crear. Cuando alguien agarra un lápiz se descubre si es pintor o no. Conviene saber unas reglas, unas normas, para saber olvidarlas en el momento justo del disparo.</p>
